package org.emergent.bzr4j.commandline.syntax;

import org.emergent.bzr4j.commandline.commands.options.Option;

/**
 * @author Guillermo Gonzalez
 *
 */
public interface IUnBindOptions
{

    public final static String COMMAND = "unbind"; //$NON-NLS-N$

    /**
     * Display more information.
     */
    public static final Option VERBOSE = new Option( "--verbose" ); //$NON-NLS-N$

    /**
     * Only display errors and warnings.
     */
    public static final Option QUIET = new Option( "--quiet" ); //$NON-NLS-N$

    public final static String HELP =
            "Convert the current checkout into a regular branch.\\n\\nAfter unbinding, the local branch is considered independent and subsequent\\ncommits will be local only."
            ; //$NON-NLS-N$
}
